export function CountUp({
  end,
  duration = 2,
  suffix = '',
  className = '',
}: {
  end: number;
  duration?: number;
  suffix?: string;
  className?: string;
}) {
  return (
    <motion.span
      className={className}
      initial={{ opacity: 0 }}
      whileInView={{ opacity: 1 }}
      viewport={{ once: true }}
    >
      <motion.span
        initial={{ opacity: 1 }}
        whileInView={{ opacity: 1 }}
        viewport={{ once: true }}
      >
        {end}{suffix}
      </motion.span>
    </motion.span>
  );
}
